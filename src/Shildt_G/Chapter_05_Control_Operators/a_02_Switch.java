package Shildt_G.Chapter_05_Control_Operators;

public class a_02_Switch {
    public static void main(String[] args) {
        for (int i = 0; i < 6; i++) {
            switch (i){
                case 0:
                    System.out.println("i = " + i);
                    break;
                case 1:
                    System.out.println("i = " + i);
                    break;
                case 2:
                    System.out.println("i = " + i);
                    break;
                case 3:
                    System.out.println("i = " + i);
                    break;
                default:
                    System.out.println("i more than 3");
            }
        }
    }
}
