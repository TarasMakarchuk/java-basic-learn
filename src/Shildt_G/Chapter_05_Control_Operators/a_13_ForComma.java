package Shildt_G.Chapter_05_Control_Operators;

public class a_13_ForComma {
    public static void main(String[] args) {
        int a, b;
        for (a = 1, b =4; a < b ; a++, b--) {
            System.out.println("a = "+a);
            System.out.println("b = "+b);
        }
    }
}
